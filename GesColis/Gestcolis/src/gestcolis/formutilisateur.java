       private void enregistrerUtilisateur() throws NoSuchAlgorithmException {
    String nometprenom = txtnometprenom.getText();
    String nomutilisateur = txtnomutilisateur.getText();
    String password = new String(txtpassword.getPassword());
    String profil = (String) cmbprofil.getSelectedItem();
        
    if (nometprenom.isEmpty() || nomutilisateur.isEmpty() || password.isEmpty() || profil == null) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Chiffrer le mot de passe avant de l'enregistrer
    String hashedPassword = Utilitaire.hashPassword(password);  // Utiliser le mot de passe haché

    String sql = "INSERT INTO utilisateur (identifiant, password, utilisateur, groupe) VALUES (?, ?, ?, ?)";
    
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bdcolis", "root", "");
         PreparedStatement ps = con.prepareStatement(sql)) {
        
        // Passer les valeurs à la requête SQL
        ps.setString(1, nometprenom);  // identifiant
        ps.setString(2, hashedPassword);  // Mot de passe haché
        ps.setString(3, nomutilisateur);  // Utilisateur
        ps.setString(4, profil);  // Groupe

        // Exécution de la requête
        int rowsInserted = ps.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Utilisateur ajouté avec succès !");
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de l'utilisateur.");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Erreur SQL : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
}

    
