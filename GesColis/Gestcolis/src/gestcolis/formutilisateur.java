    private void chargerGroupes() {
    // Connexion à la base de données
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bdcolis", "root", "");
         Statement stmt = con.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT groupe FROM profil")) {

        // Vider le JComboBox avant de le remplir
        cmbprofil.removeAllItems();

        // Ajouter les groupes au JComboBox
        while (rs.next()) {
            String groupe = rs.getString("groupe");
            cmbprofil.addItem(groupe);
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des groupes : " + ex.getMessage(),
                "Erreur", JOptionPane.ERROR_MESSAGE);
    }
}

    
    
    

